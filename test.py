    def add(self, num_a, num_b):
        '''Adds two numbers'''
        return num_a +num_b

    def subtract(self, num_a, num_b):
        '''Adds two numbers'''
        return num_a - num_b

    def divide(self, num_a, num_b):
        '''Divides num_a by num_b'''
        return num_a / num_b


